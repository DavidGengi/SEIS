    private void CalculaAhorroFregadero(){
        this.txtPFregadero.setEditable(false);
        int seleccion = this.ComboFregadero.getSelectedIndex();
        double porcentaje = 0;
        switch(seleccion){
            case 0: 
                porcentaje = 0;                
                this.txtGastoAhorroFregadero.setText(String.format("%.3f",this.agua.calculaAhorro(porcentaje, 1)));
                break;
            case 1:
                porcentaje = 53.50;
                this.txtGastoAhorroFregadero.setText(String.format("%.3f",this.agua.calculaAhorro(porcentaje, 1)));
                break;
            case 2:
                porcentaje = 50;
                this.txtGastoAhorroFregadero.setText(String.format("%.3f",this.agua.calculaAhorro(porcentaje, 1)));
                break;
            case 3:
                porcentaje = 80;
                this.txtGastoAhorroFregadero.setText(String.format("%.3f",this.agua.calculaAhorro(porcentaje, 1)));
                break;
            case 4:
                JOptionPane.showMessageDialog(null, "Escribe el porcentaje de ahorro y Da 'Enter' al Finalizar", "Porcentaje", JOptionPane.INFORMATION_MESSAGE);
                this.txtPFregadero.setEditable(true);
                break;
        }
        this.txtPFregadero.setText(String.format("%.1f",porcentaje));
        this.gastoTotalAhorro.setText(String.format("%.3f",this.agua.getGastoTotalAhorro()));
    }
