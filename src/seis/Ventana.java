    private double CalculaSuma(){
        double suma =0.0; 
        suma += (double) this.txt3Lecho.getValue();
        suma += (double) this.txt3Lagunaje.getValue();
        suma += (double) this.txt3Lodos.getValue();
        suma += (double) this.txt3Lagunas.getValue();
        suma += (double) this.txt3Aerobia.getValue();
        suma += (double) this.txt3Filtros.getValue();
        suma += (double) this.txt3Anaerobia.getValue();      
        return suma;
    }
    
    
